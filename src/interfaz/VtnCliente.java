    private void eliminarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_eliminarActionPerformed
    {//GEN-HEADEREND:event_eliminarActionPerformed

        int filaSeleccionada = tabla.getSelectedRow();
        if (filaSeleccionada != -1)
        {
            int idCliente = Integer.parseInt(tabla.getValueAt(filaSeleccionada, 0).toString());

            int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, "¿Estás seguro de que deseas eliminar este cliente?", "Confirmar eliminación", javax.swing.JOptionPane.YES_NO_OPTION);

            if (confirmacion == javax.swing.JOptionPane.YES_OPTION)
            {
                ClienteDao.eliminarCliente(idCliente);
                llenarTablaClientes();
            }
        } else
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un cliente para eliminar.");
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private boolean obtenerEstadoSeleccionado()
    {
        String estadoSeleccionado = (String) estado.getSelectedItem();
        return "Activo".equals(estadoSeleccionado);
    }

    private void llenarTablaClientes()
    {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();

        modelo.setRowCount(0);

        List<modelo.Cliente> clientes = ClienteDao.listarClientes();

        for (modelo.Cliente cliente : clientes)
        {
            modelo.addRow(new Object[]
            {
                cliente.getIdCliente(),
                cliente.getDni(),
                cliente.getNombres(),
                cliente.getDireccion(),
                cliente.isEstado() ? "Activo" : "Inactivo"
            });
        }
    }

