    private void actualizarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_actualizarActionPerformed
    {//GEN-HEADEREND:event_actualizarActionPerformed
        int filaSeleccionada = tabla.getSelectedRow();
        if (filaSeleccionada != -1)
        {
            try
            {
                int idCliente = Integer.parseInt(tabla.getValueAt(filaSeleccionada, 0).toString());
                String dni = this.dni.getText().trim();
                String nombre = this.nombre.getText().trim();
                String direccion = this.direccion.getText().trim();

                if (dni.isEmpty() || nombre.isEmpty() || direccion.isEmpty())
                {
                    javax.swing.JOptionPane.showMessageDialog(this, "DNI, nombre y dirección no pueden estar vacíos.");
                    return;
                }

                boolean estado = obtenerEstadoSeleccionado();

                modelo.Cliente clienteActualizado = new modelo.Cliente(idCliente, dni, nombre, direccion, estado);

                ClienteDao.actualizarCliente(clienteActualizado);

                llenarTablaClientes();
            } catch (NumberFormatException e)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al obtener el ID del cliente. Por favor, selecciona un cliente válido.");
            }
        } else
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un cliente para actualizar.");
        }
    }//GEN-LAST:event_actualizarActionPerformed

